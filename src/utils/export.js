/**
 * Export text as SRT subtitle format
 */
export function exportToSrt(content, filename = 'subtitles.srt') {
  const lines = content.split('\n').filter(l => l.trim());
  let srt = '';
  let index = 1;
  let seconds = 0;

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    // Estimate duration: ~150 chars per 10 seconds
    const duration = Math.max(3, Math.ceil(trimmed.length / 15));
    const startTime = formatSrtTime(seconds);
    seconds += duration;
    const endTime = formatSrtTime(seconds);

    srt += `${index}\n`;
    srt += `${startTime} --> ${endTime}\n`;
    srt += `${trimmed}\n\n`;
    index++;
  }

  const blob = new Blob([srt], { type: 'text/plain;charset=utf-8' });
  saveAs(blob, filename);
}

/**
 * Export text as VTT subtitle format
 */
export function exportToVtt(content, filename = 'subtitles.vtt') {
  const lines = content.split('\n').filter(l => l.trim());
  let vtt = 'WEBVTT\n\n';
  let seconds = 0;

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) continue;

    const duration = Math.max(3, Math.ceil(trimmed.length / 15));
    const startTime = formatVttTime(seconds);
    seconds += duration;
    const endTime = formatVttTime(seconds);

    vtt += `${startTime} --> ${endTime}\n`;
    vtt += `${trimmed}\n\n`;
  }

  const blob = new Blob([vtt], { type: 'text/vtt;charset=utf-8' });
  saveAs(blob, filename);
}

// Helpers
function formatSrtTime(totalSeconds) {
  const h = String(Math.floor(totalSeconds / 3600)).padStart(2, '0');
  const m = String(Math.floor((totalSeconds % 3600) / 60)).padStart(2, '0');
  const s = String(Math.floor(totalSeconds % 60)).padStart(2, '0');
  return `${h}:${m}:${s},000`;
}

function formatVttTime(totalSeconds) {
  const h = String(Math.floor(totalSeconds / 3600)).padStart(2, '0');
  const m = String(Math.floor((totalSeconds % 3600) / 60)).padStart(2, '0');
  const s = String(Math.floor(totalSeconds % 60)).padStart(2, '0');
  return `${h}:${m}:${s}.000`;
}
